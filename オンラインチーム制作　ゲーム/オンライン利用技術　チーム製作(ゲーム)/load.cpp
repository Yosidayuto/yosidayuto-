#include "load.h"
#include "ui.h"
#include "wait.h"
//=============================================================================
//静的メンバ変数宣言
//=============================================================================

//=============================================================================
// コンストラクタ
//=============================================================================
CLoad::CLoad()
{
	for (int nCount = 0; nCount < 5; nCount++)
	{
		m_pUi[nCount] = NULL;
	}
	m_nCountUi = 0.0f;

}

//=============================================================================
// デストラクタ
//=============================================================================
CLoad::~CLoad()
{
}

//=============================================================================
// 作成処理
//=============================================================================
CLoad * CLoad::Create(D3DXVECTOR3 size)
{
	CLoad *pLoad;
	//インスタンス生成
	pLoad = new CLoad;

	//初期化処理
	if (pLoad != NULL)
	{
		pLoad->Init(size);
	}

	return pLoad;
}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CLoad::Init(D3DXVECTOR3 size)
{
	CWait::Create(D3DXVECTOR3(SCREEN_WIDTH, SCREEN_HEIGHT, 0.0f));
	for (int nCount = 0; nCount < 5; nCount++)
	{
		m_pUi[nCount] = CUi::Create(D3DXVECTOR3((SCREEN_WIDTH / 2 - 100) + (50 * nCount), SCREEN_HEIGHT / 2, 0.0f), D3DXVECTOR3(50, 50, 00), CUi::TEX_LOAD);

	}
	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void CLoad::Uninit(void)
{
	Release();
}

//=============================================================================
// 更新処理
//=============================================================================
void CLoad::Update(void)
{
	for (int nCount=0;nCount<5;nCount++)
	{
			m_pUi[nCount]->LoadPos();
	}



}

