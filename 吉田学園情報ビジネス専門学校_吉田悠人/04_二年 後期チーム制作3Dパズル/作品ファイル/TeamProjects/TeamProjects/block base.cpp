#include "manager.h"
#include "renderer.h"
#include "object.h"
#include "model.h"
#include "block base.h"
#include "model object.h"

//=============================================================================
// コンストラクタ
//=============================================================================
CBlockBase::CBlockBase()
{
	//モデルデータの初期化
	SetObjType(OBJTYPE_BLOCK);
	m_model = NULL;
	m_modeldata = {};
}
//=============================================================================
// デストラクタ
//=============================================================================
CBlockBase::~CBlockBase()
{
}

//=============================================================================
// 初期化関数
//=============================================================================
HRESULT CBlockBase::Init(void)
{
	//モデル生成
	m_model = CModelObject::Create(m_modeldata, this, GetPosition(), GetRotation(), GetScale());
	return S_OK;
}

//=============================================================================
// 終了関数
//=============================================================================
void CBlockBase::Uninit(void)
{
	//NULLチェック
	if (m_model != NULL)
	{
		m_model->Uninit();
	}

	Release();
}

//=============================================================================
// 更新関数
//=============================================================================
void CBlockBase::Update(void)
{
	//位置取得
	SetPosition(m_model->GetPosition());
}


//=============================================================================
// モデルデータセッター
//=============================================================================
void CBlockBase::SetModelData(CModel::MODELDATA ModelData)
{
	m_modeldata = ModelData;
}

