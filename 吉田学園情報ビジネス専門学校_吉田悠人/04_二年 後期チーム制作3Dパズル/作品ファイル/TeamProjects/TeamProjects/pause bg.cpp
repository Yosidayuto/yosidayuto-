#include "pause bg.h"
#include "renderer.h"
#include "manager.h"
//=============================================================================
// マクロ定義
//=============================================================================
#define BG_COLLAR (D3DCOLOR_RGBA(0, 0, 0, 128))	//背景のカラー

//=============================================================================
// コンストラクタ
//=============================================================================
CPauseBg::CPauseBg(int nPriority) :CUi(nPriority)
{

}

//=============================================================================
// デストラクタ
//=============================================================================
CPauseBg::~CPauseBg()
{
}

//=============================================================================
// 生成処理
//=============================================================================
CPauseBg * CPauseBg::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size)
{
	//メモリ確保
	CPauseBg* pPauseBg = NULL;
	pPauseBg = new CPauseBg;

	//NULLチェック
	if (pPauseBg != NULL)
	{
		//位置設定
		pPauseBg->SetPos(D3DXVECTOR3(pos.x, pos.y, pos.z));
		//サイズ設定
		pPauseBg->SetSize(D3DXVECTOR3(size.x, size.y, size.z));
		//カラー設定
		pPauseBg->SetCol(BG_COLLAR);
		//初期化処理
		pPauseBg->Init();
	}
	return pPauseBg;
}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CPauseBg::Init(void)
{
	CUi::Init();
	return S_OK;
}

//=============================================================================
// 更新処理
//=============================================================================
void CPauseBg::Update(void)
{
	CUi::Update();
}
