#include "ui count.h"
#include "scene.h"
//=============================================================================
// コンストラクタ
//=============================================================================
CUiCount::CUiCount(int nPriorit) :CScene(nPriorit)
{
	m_apNumber	= NULL;
	m_nCount	= NULL;
}

//=============================================================================
// デストラクタ
//=============================================================================
CUiCount::~CUiCount()
{
}

//=============================================================================
// 生成処理
//=============================================================================
CUiCount * CUiCount::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size, PRIORITY priority)
{
	CUiCount *pUiCount;
	pUiCount = new CUiCount(priority);
	pUiCount->m_pos = pos;
	pUiCount->m_size = size;
	pUiCount->Init();

	return pUiCount;
}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CUiCount::Init(void)
{
	//カウントナンバー生成
	m_apNumber = CNumber::Create(m_pos, m_size);

	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void CUiCount::Uninit(void)
{
	if (m_apNumber != NULL)
	{
		m_apNumber->Uninit();
	}

	Release();

}

//=============================================================================
// 更新処理
//=============================================================================
void CUiCount::Update(void)
{
	if (m_apNumber != NULL)
	{
		m_apNumber->Update();
	}
}

//=============================================================================
// 描画関数
//=============================================================================
void CUiCount::Draw(void)
{
	if (m_apNumber != NULL)
	{
		m_apNumber->Draw();
	}
}

//=============================================================================
//カウントセッター
//=============================================================================
void CUiCount::SetCount(int nCount)
{
	m_apNumber->SetNumber(nCount);
}

//=============================================================================
//　カラーセッター
//=============================================================================
void CUiCount::SetCol(D3DXCOLOR col)
{
	m_apNumber->ColChange(col);
}
