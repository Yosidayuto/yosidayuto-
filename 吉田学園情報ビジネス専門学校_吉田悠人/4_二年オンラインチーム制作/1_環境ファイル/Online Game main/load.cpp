//=============================================================================
// コンストラクタ
//=============================================================================
CLoad::CLoad()
{
	for (int nCount = 0; nCount < 5; nCount++)
	{
		m_pUi[nCount] = NULL;
	}
	m_nCountUi = 0;

}

//=============================================================================
// デストラクタ
//=============================================================================
CLoad::~CLoad()
{
}

//=============================================================================
// 作成処理
//=============================================================================
CLoad * CLoad::Create(D3DXVECTOR3 size)
{
	CLoad *pLoad;
	//インスタンス生成
	pLoad = new CLoad;

	//初期化処理
	if (pLoad != NULL)
	{
		pLoad->Init(size);
	}

	return pLoad;
}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CLoad::Init(D3DXVECTOR3 size)
{
	CWait::Create(D3DXVECTOR3(SCREEN_WIDTH, SCREEN_HEIGHT, 0.0f));
	for (int nCount = 0; nCount < 5; nCount++)
	{
		m_pUi[nCount] = CUi::Create(D3DXVECTOR3((SCREEN_WIDTH / 2.0f - 100.0f) + (50.0f * (float)nCount), SCREEN_HEIGHT / 2.0f, 0.0f), D3DXVECTOR3(50.0f, 50.0f, 0.0f), CUi::TEX_LOAD);

	}
	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void CLoad::Uninit(void)
{
	Release();
}

//=============================================================================
// 更新処理
//=============================================================================
void CLoad::Update(void)
{
	for (int nCount=0;nCount<5;nCount++)
	{
			m_pUi[nCount]->LoadPos();
	}
}

